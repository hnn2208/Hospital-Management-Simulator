// i created this simple class to sort the patient's infos
public class Patient {
    int id; // unique id
    String name;
    int severity; // 1 to 10 scale
    int age;

    public Patient(int id, String name, int severity, int age) {
        this.id = id;
        this.name = name;
        this.severity = severity;
        this.age = age;
    }
}


